uint64_t play_hash(const std::vector<Balatro::Card>& cards) {
    int val = 0;
    for (const auto& card : cards)
    {
        val = (val * 14) + static_cast<int>(card.rank);
    }
    for (int i = static_cast<int>(cards.size()); i < 5; ++i)
    {
        val = (val * 14) + 13;
    }
    return phash(val);
}


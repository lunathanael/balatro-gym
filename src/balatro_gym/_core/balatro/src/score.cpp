int eval_hand(std::vector<int> cards) {
    if (cards[0] == 13)
        return 0;

    std::vector<int> rank_count(static_cast<int>(Balatro::Rank::NUMBER_OF_RANKS), 0);
    for (const int card : cards)
    {
        if (card == 13)
            continue;  // no_card
        rank_count[card]++;
    }

    int pairs = 0, threes = 0, fours = 0;
    for (int count : rank_count)
    {
        if (count == 2)
            pairs++;
        if (count == 3)
            threes++;
        if (count == 4)
            fours++;
    }

    bool has_straight = false;
    for (int i = static_cast<int>(Balatro::Rank::Two); i <= static_cast<int>(Balatro::Rank::Ten);
         i++)
    {
        if (rank_count[i] && rank_count[i + 1] && rank_count[i + 2] && rank_count[i + 3]
            && rank_count[i + 4])
        {
            has_straight = true;
            break;
        }
    }
    if (rank_count[12] && rank_count[0] && rank_count[1] && rank_count[2] && rank_count[3])
    {
        has_straight = true;
    }

    if (fours == 1)
    {
        int score = 60;
        for (int card : cards)
        {
            if (card == 13)
                continue;
            if (rank_count[card] == 4)
            {
                if (card == static_cast<int>(Balatro::Rank::Ace))
                    score += 11;
                else
                    score += std::min(card + 2, 10);
            }
        }
        return score * 7 + BEATS_FLUSH;
    }
    if (threes == 1 && pairs == 1)
    {
        int score = 40;
        for (int card : cards)
        {
            if (card == static_cast<int>(Balatro::Rank::Ace))
                score += 11;
            else
                score += std::min(card + 2, 10);
        }
        return score * 4 + BEATS_FLUSH;
    }
    if (has_straight)
    {
        int score = 30;
        for (int card : cards)
        {
            if (card == static_cast<int>(Balatro::Rank::Ace))
                score += 11;
            else
                score += std::min(card + 2, 10);
        }
        return score * (-4);
    }

    if (threes == 1)
    {
        int score = 30;
        for (int card : cards)
        {
            if (card == 13)
                continue;
            if (rank_count[card] == 3)
            {
                if (card == static_cast<int>(Balatro::Rank::Ace))
                    score += 11;
                else
                    score += std::min(card + 2, 10);
            }
        }
        return score * 3;
    }
    if (pairs == 2)
    {
        int score = 20;
        for (int card : cards)
        {
            if (card == 13)
                continue;
            if (rank_count[card] == 2)
            {
                if (card == static_cast<int>(Balatro::Rank::Ace))
                    score += 11;
                else
                    score += std::min(card + 2, 10);
            }
        }
        return score * 2;
    }
    if (pairs == 1)
    {
        int score = 10;
        for (int card : cards)
        {
            if (card == 13)
                continue;
            if (rank_count[card] == 2)
            {
                if (card == static_cast<int>(Balatro::Rank::Ace))
                    score += 11;
                else
                    score += std::min(card + 2, 10);
            }
        }
        return score * 2;
    }
    int score =
      (cards[0] == static_cast<int>(Balatro::Rank::Ace)) ? 11 : std::min(cards[0] + 2, 10);
    return (score + 5) * 1;
}
